  /// Get status label by status value
  static String getStatusLabel(String status) {
    switch (status.toLowerCase()) {
      case 'active':
        return 'Active';
      case 'inactive':
        return 'Inactive';
      case 'pending':
        return 'Pending';
      case 'approved':
        return 'Approved';
      case 'rejected':
        return 'Rejected';
      case 'cancelled':
        return 'Cancelled';
      case 'processing':
        return 'Processing';
      case 'completed':
        return 'Completed';
      case 'on_hold':
        return 'On Hold';
      case 'expired':
        return 'Expired';
      case 'failed':
        return 'Failed';
      default:
        return status;
    }
  }

  /// Check if status is final (no further changes expected)
  static bool isFinalStatus(String status) {
    return [
      statusCompleted,
      statusCancelled,
      statusRejected,
      statusExpired,
      statusFailed,
    ].contains(status.toLowerCase());
  }

  /// Check if status is in progress
  static bool isInProgressStatus(String status) {
    return [
      statusPending,
      statusProcessing,
      statusOnHold,
    ].contains(status.toLowerCase());
  }
