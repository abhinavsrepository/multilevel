  /// Get property type icon
  static String getPropertyTypeIcon(String type) {
    switch (type.toLowerCase()) {
      case 'apartment':
        return '$_iconPath/apartment.png';
      case 'villa':
        return '$_iconPath/villa.png';
      case 'plot':
        return '$_iconPath/plot.png';
      case 'commercial':
        return '$_iconPath/commercial.png';
      case 'farmhouse':
        return '$_iconPath/farmhouse.png';
      default:
        return propertyPlaceholder;
    }
  }

  /// Get rank badge by rank name
  static String getRankBadge(String rank) {
    switch (rank.toLowerCase()) {
      case 'bronze':
        return bronzeBadge;
      case 'silver':
        return silverBadge;
      case 'gold':
        return goldBadge;
      case 'platinum':
        return platinumBadge;
      case 'diamond':
        return diamondBadge;
      case 'crown_diamond':
      case 'crowndiamond':
        return crownDiamondBadge;
      default:
        return bronzeBadge;
    }
  }

  /// Get status icon by status name
